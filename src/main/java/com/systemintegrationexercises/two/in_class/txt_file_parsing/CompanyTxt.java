package com.systemintegrationexercises.two.in_class.txt_file_parsing;

public class CompanyTxt {
    String companyName;
    String cvr;
    String email;
    String number;
    String postalCode;
    String street;
    String town;

    public CompanyTxt(String companyName, String cvr, String email, String number, String postalCode, String street, String town) {
        this.companyName = companyName;
        this.cvr = cvr;
        this.email = email;
        this.number = number;
        this.postalCode = postalCode;
        this.street = street;
        this.town = town;
    }
}


